# =====================================================================
# 2. PREPROCESAMIENTO DE TU FOTO EXTERNA
# =====================================================================
def preparar_imagen_externa(ruta_imagen):
    # 1. Leer la foto en escala de grises
    img = cv2.imread(ruta_imagen, cv2.IMREAD_GRAYSCALE)

    # 2. Redimensionar primero a un tamaño mediano para procesar mejor (ej. 100x100)
    img_medium = cv2.resize(img, (100, 100))

    # 3. BINARIZACIÓN OTSU: Separa el texto del fondo de forma inteligente.
    # Convierte el papel en blanco absoluto (255) y la tinta en negro absoluto (0).
    _, img_thresh = cv2.threshold(img_medium, 0, 255, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU)

    # 4. DILATACIÓN: Engrosa el trazo simulando un marcador grueso
    kernel = np.ones((3, 3), np.uint8)
    img_thick = cv2.dilate(img_thresh, kernel, iterations=1)

    # 5. Redimensionar finalmente a los 28x28 obligatorios de tu red
    img_resized = cv2.resize(img_thick, (28, 28))

    # 6. Normalizar los píxeles de 0 a 1
    img_normalized = img_resized / 255.0

    # 7. Adaptar la forma para tu modelo de Keras: (1, 28, 28, 1)
    img_final = np.expand_dims(img_normalized, axis=(0, -1))

    return img_final

