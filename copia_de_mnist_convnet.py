    imagen_pil.save(ruta_temporal)

    # Procesar la foto con nuestra función
    tensor_imagen = preparar_imagen_externa(ruta_temporal)

    # Hacer la predicción con el modelo cargado
    prediccion = modelo_completo.predict(tensor_imagen)
    clase = np.argmax(prediccion, axis=1)[0]
    confianza = np.max(prediccion) * 100

    if os.path.exists(ruta_temporal):
        os.remove(ruta_temporal)

    return f"🔢 Dígito Detectado: {clase}\n🎯 Confianza: {confianza:.2f}%"

